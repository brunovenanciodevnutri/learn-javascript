var vel = 60.5

console.log(`A velocidade do seu carro é de ${vel}Km/h.`)
if (vel > 60) { // Condição Simples
    console.log(`Você ultrapassou a velocidade permitida. MULTADO!`)
}
else { // Condição Composta
    console.log(`Você está dentro do limite de velocidade permitida. Parabéns!`)
}
console.log(`Dirija sempre com cinto de segurança.`)